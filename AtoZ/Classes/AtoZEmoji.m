-   (BOOL) containsEmoji {

//  return [self containsEmojiCharactersInRange:NSMakeRange(0, self.length)];  // This might just be better.. check

  __block int winCase = -99;  __block BOOL returnValue = NO; __block unichar match = NULL;

  [self enumerateSubstringsInRange:NSMakeRange(0, self.length)
                           options:NSStringEnumerationByComposedCharacterSequences
                        usingBlock:^(NSS *substring, NSRNG substringRange, NSRNG enclosingRange, BOOL *stop) {

    const unichar hs = match = [substring characterAtIndex:0];

    *stop = returnValue = INRANGE(hs,0xd800,0xdbff) ?     // surrogate pair
      substring.length <= 1 ? NO : ({

      const unichar ls = [substring characterAtIndex:1];
      const int uc     = ((hs - 0xd800) * 0x400) +
                          (ls - 0xdc00) + 0x10000;

      winCase = INRANGE(uc,0x1d000,0x1f77f);
    }) : substring.length > 1 ? ({

      const unichar deux = [substring characterAtIndex:1]; BOOL win = deux == 0x20e3;
      if (win) winCase = 2; win;
    }) :

      INRANGE(hs, 0x2100, 0x27ff) || INRANGE(hs, 0x2B05, 0x2b07) || // non surrogate
      INRANGE(hs, 0x2934, 0x2935) || INRANGE(hs, 0x3297, 0x3299) ||
      hs == 0xa9   || hs == 0xae   || hs == 0x303d || hs == 0x3030 ||
      hs == 0x2b55 || hs == 0x2b1c || hs == 0x2b1b || hs == 0x2b50;
   }];


#ifdef DEBUG
  UInt codeValue;
  [[NSScanner scannerWithString:self] scanHexInt:&codeValue];
  returnValue ? NSLog(@"matched %@ with %u", self, (unichar)codeValue) :
                NSLog(@"failed to match %u with emoji", (unichar)codeValue);
#endif
  return returnValue;
}
